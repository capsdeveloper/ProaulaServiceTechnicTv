    private void botonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarActionPerformed

            if(botonRegistrar.getText().equals("Nuevo")){
            
            habilitar();
            }else{
                try {
            String nombre = campoNombreTecnico.getText();
            String apellido = campoApellidoTecnico.getText();
            String identificacion = campoIdentificacionTecnico.getText();
            String especialidad = campoEspecialidadTecnico.getText();
            

            if (nombre == null || nombre.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre es requerido");
                return;
            }

            if (apellido == null || apellido.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "El apellido es requerido");
                return;
            }
            
            if (identificacion == null || identificacion.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "La identificacion es requerida");
                return;
            }
    
            if (especialidad == null || especialidad.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "El numero de telefono es requerido");
                return;
            }
            
            

            var tecnico = new Tecnico(nombre,  apellido, identificacion, especialidad);
            var usuarioDao = new TecnicoDao();
            usuarioDao.registrar(tecnico);
            JOptionPane.showMessageDialog(this, "Tecnico guardado con exito");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
            
        }
             

        
    }//GEN-LAST:event_botonRegistrarActionPerformed

    private void botonEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarClienteActionPerformed
       String identificacion = campoIdentificacionTecnico.getText().trim();

    if (identificacion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor ingrese la identificacion del técnico a eliminar.");
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este técnico?", "Confirmar", JOptionPane.YES_NO_OPTION);

    if (confirmacion == JOptionPane.YES_OPTION) {
        TecnicoDao tecnicoDao = new TecnicoDao();
        boolean eliminado = tecnicoDao.eliminarTecnico(identificacion);

        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Técnico eliminado correctamente.");
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró un técnico con esa identificacion.");
        }
    }
    }//GEN-LAST:event_botonEliminarClienteActionPerformed

