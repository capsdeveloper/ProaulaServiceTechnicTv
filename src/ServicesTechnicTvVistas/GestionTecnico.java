    private void botonBuscarTecnicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarTecnicoActionPerformed
        try {
            var identificacion = campoIdentificacionTecnico.getText();
            if (identificacion == null || identificacion.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "La identificacion es requerido para buscar");
                return;
            }
            var tecnicoDao = new TecnicoDao();
            var tecnico = tecnicoDao.buscarPorId(identificacion);

            campoNombreTecnico.setText(tecnico.getNombre());
            campoApellidoTecnico.setText(tecnico.getApellido());
            campoIdentificacionTecnico.setText(tecnico.getIdentificacion());
            campoEspecialidadTecnico.setText (tecnico.getEspecialidad());
        } catch (Exception ex) {
              JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_botonBuscarTecnicoActionPerformed

